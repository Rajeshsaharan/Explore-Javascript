/// == and ===

// "==" check only value not data types

let number1 = "6"
let number2 = 6;
console.log(number1 == number2) // output --> true because it checks only value

// "===" check data type or value

let num3 = "6"
let num4 = 6
console.log(num3 === num4) //output --> false because it checks both value & data type

