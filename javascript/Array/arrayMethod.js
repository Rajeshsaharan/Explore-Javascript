//array methods

//array push ---> add items in the ending

let fruits = ["apple", "banana"]

fruits.push("mango") ///....pushed mango in as last element

//pop
fruits.pop() //remove last element from original array and return last item

//unshift --- add item to zero index

fruits.unshift("starwberry", "blackberry")  //add items to zero

//shift -- remove items from zero

fruits.shift() //remove item from zero index & return first item


